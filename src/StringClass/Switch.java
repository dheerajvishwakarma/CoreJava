package StringClass;

public class Switch {
	public static void main(String[] args) {
		char c = 'z';
		System.out.println(c);
		switch (c) {
		case 'a':
			System.out.println("yeah its vowel");
			break;
		case 'e':
			System.out.println("yeah its vowel");
			break;
		case 'i':
			System.out.println("yeah its vowel");
			break;
		case 'o':
			System.out.println("yeah its vowel");
			break;
		case 'u':
			System.out.println("yeah its vowel");
			break;
		default:

			System.out.println("oops its not vowel");
		}
	}

}
